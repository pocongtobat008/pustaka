function TaskItem({ task, selectedMonth, onToggle, onEdit, onDelete, onUpdateIssues, onUpdateKendala, hasPermission }) {
    const isOverdue = useMemo(() => {
        if (task.status === 'done') return false;
        const creationDate = task.created_at ? new Date(task.created_at) : new Date(Number(task.id));
        const now = new Date();
        const today = startOfDay(now);
        
        let taskDate;
        if (task.type === 'recurring') {
            // Proyeksikan tanggal rutin ke bulan yang sedang dilihat
            const day = new Date(task.dueDate).getDate();
            // Safe date creation handling varying days in month
            const year = selectedMonth.getFullYear();
            const month = selectedMonth.getMonth();
            taskDate = new Date(year, month, Math.min(day, new Date(year, month + 1, 0).getDate()));
            
            // Jangan anggap terlambat jika tanggal jadwal sebelum tanggal pembuatan
            if (isBefore(taskDate, startOfDay(creationDate))) return false;
        } else {
            taskDate = startOfDay(new Date(task.dueDate));
        }
        
        return isBefore(taskDate, today);
    }, [task, selectedMonth]);

    const handleAddIssue = () => {
        const note = prompt("Masukkan catatan issue:");
        if (!note) return;
        const newIssue = { note, progress: 0, status: 'pending', id: Date.now(), createdAt: new Date().toISOString() };
        const updatedIssues = [...(task.issues || []), newIssue];
        if (onUpdateIssues) onUpdateIssues(task.id, updatedIssues);
    };

    const isFuture = isFutureMonth(selectedMonth);

    return (
        <div className={`group relative p-4 rounded-3xl border transition-all ${
            task.status === 'done' 
            ? 'bg-emerald-50/50 dark:bg-emerald-500/5 border-emerald-100 dark:border-emerald-500/20' 
            : 'bg-slate-50 dark:bg-white/5 border-transparent hover:border-indigo-500/30'
        }`}>
            <div className="flex items-start gap-3">
                {/* Logic: Hide checklist if future month. Show if current or past. */}
                {isFuture ? (
                    <div className="mt-1 w-5 h-5 rounded-lg border-2 border-slate-200 dark:border-white/10 flex items-center justify-center bg-slate-100 dark:bg-white/5 opacity-50 cursor-not-allowed"></div>
                ) : (
                    <button 
                    onClick={(e) => {
                        e.stopPropagation();
                        if (hasPermission('job-due-date', 'edit')) onToggle(task.id);
                    }}
                    disabled={!hasPermission('job-due-date', 'edit')}
                    className={`mt-1 w-5 h-5 rounded-lg border-2 flex items-center justify-center transition-all ${
                        task.status === 'done' 
                        ? 'bg-emerald-500 border-emerald-500 text-white' 
                        : 'border-slate-300 dark:border-white/20'
                    }`}
                >
                    {task.status === 'done' && <CheckCircle2 size={12} />}
                </button>
                )}
                
                <div className="flex-1 min-w-0">
                    <div className="flex items-center justify-between mb-1">
                        <span className="text-[10px] font-black text-slate-400 uppercase tracking-tighter">
                            {task.type === 'recurring' ? (
                                <span className="flex items-center gap-1 text-indigo-500">
                                    <Repeat size={10} /> Tiap Tanggal {new Date(task.dueDate).getDate()}
                                </span>
                            ) : (
                                format(new Date(task.dueDate), 'dd EEEE', { locale: id })
                            )}
                        </span>
                        {isOverdue && (
                            <span className="text-[8px] font-black bg-red-500 text-white px-1.5 py-0.5 rounded uppercase tracking-tighter animate-pulse flex items-center gap-1">
                                <AlertCircle size={8} /> Terlambat
                            </span>
                        )}
                        {task.status === 'done' && task.completedAt && (
                            <span className="text-[9px] font-bold text-emerald-500 bg-emerald-500/10 px-2 py-0.5 rounded-md flex items-center gap-1">
                                <CheckCircle2 size={8} /> {format(new Date(task.completedAt), 'dd/MM/yy HH:mm')}
                            </span>
                        )}
                        <div className="flex items-center gap-1">
                            {task.privacy === 'private' && <Lock size={10} className="text-amber-500" />}
                            {task.privacy === 'department' && <Building2 size={10} className="text-blue-500" />}
                            {task.privacy === 'public' && <Globe size={10} className="text-emerald-500" />}
                        </div>
                    </div>
                    <h4 className={`text-sm font-bold truncate ${task.status === 'done' ? 'text-slate-400 line-through' : 'text-slate-700 dark:text-white'}`}>
                        {task.title}
                    </h4>

                    {/* Kendala Note Input - Muncul saat pending */}
                    {task.status === 'pending' && (
                        <div className="mt-2">
                            <input 
                                type="text"
                                placeholder="Tulis kendala/catatan di sini..."
                                value={task.kendala || ''}
                                onChange={(e) => onUpdateKendala(task.id, e.target.value)}
                                disabled={!hasPermission('job-due-date', 'edit')}
                                className="w-full bg-transparent border-b border-slate-200 dark:border-white/10 text-[11px] py-1 outline-none focus:border-indigo-500 transition-colors italic text-slate-500 dark:text-slate-400"
                            />
                        </div>
                    )}
                    
                    {/* Issue Summary */}
                    {task.issues && task.issues.length > 0 && (
                        <div className="mt-3 space-y-2">
                            <div className="flex items-center justify-between text-[9px] font-black uppercase tracking-widest text-slate-400">
                                <span>Issues ({task.issues.filter(i => i.status === 'resolved').length}/{task.issues.length})</span>
                                <span>{calculateProgress(task.issues)}%</span>
                            </div>
                            <div className="h-1.5 w-full bg-slate-200 dark:bg-white/10 rounded-full overflow-hidden">
                                <motion.div 
                                    initial={{ width: 0 }}
                                    animate={{ width: `${calculateProgress(task.issues)}%` }}
                                    className="h-full bg-indigo-500 rounded-full"
                                />
                            </div>
                            <div className="flex flex-col gap-1">
                                {task.issues.slice(0, 2).map((iss, idx) => (
                                    <div key={idx} className="flex items-center gap-2 text-[10px] text-slate-500 dark:text-slate-400 italic">
                                        <AlertCircle size={10} className={iss.status === 'resolved' ? 'text-emerald-500' : 'text-amber-500'} />
                                        <span className="truncate">{iss.note}</span>
                                    </div>
                                ))}
                            </div>
                        </div>
                    )}
                </div>
            </div>

            <div className="absolute top-2 right-2 opacity-0 group-hover:opacity-100 transition-opacity flex gap-1">
                {hasPermission('job-due-date', 'edit') && (
                    <button 
                        onClick={onEdit}
                        className="p-1.5 bg-white dark:bg-slate-800 rounded-lg shadow-sm text-slate-400 hover:text-indigo-500"
                    >
                        <Edit3 size={12} />
                    </button>
                )}
                {hasPermission('job-due-date', 'delete') && (
                    <button 
                        onClick={onDelete}
                        className="p-1.5 bg-white dark:bg-slate-800 rounded-lg shadow-sm text-slate-400 hover:text-red-500"
                    >
                        <Trash2 size={12} />
                    </button>
                )}
            </div>
        </div>
    );
}